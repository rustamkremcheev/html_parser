public class SalaryObj {

    private String pinfl;
    String company_tin;
    String company_name;

    String name;
    String number_passport;
    String seria_passport;

    String year;
    String period;

    String salary;
    String salary_tax_sum;

    public SalaryObj() {}

    public String getPinfl() {
        return pinfl;
    }

    public void setPinfl(String pinfl) {
        this.pinfl = pinfl;
    }

    public String getCompany_tin() {
        return company_tin;
    }

    public void setCompany_tin(String company_tin) {
        this.company_tin = company_tin;
    }

    public String getCompany_name() {
        return company_name;
    }

    public void setCompany_name(String company_name) {
        this.company_name = company_name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNumber_passport() {
        return number_passport;
    }

    public void setNumber_passport(String number_passport) {
        this.number_passport = number_passport;
    }

    public String getSeria_passport() {
        return seria_passport;
    }

    public void setSeria_passport(String seria_passport) {
        this.seria_passport = seria_passport;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public String getPeriod() {
        return period;
    }

    public void setPeriod(String period) {
        this.period = period;
    }

    public String getSalary() {
        return salary;
    }

    public void setSalary(String salary) {
        this.salary = salary;
    }

    public String getSalary_tax_sum() {
        return salary_tax_sum;
    }

    public void setSalary_tax_sum(String salary_tax_sum) {
        this.salary_tax_sum = salary_tax_sum;
    }
}
